    def _test_project(self, agent, output_dir: str) -> str:
        """Run basic tests on the project"""
        output_path = Path(output_dir)
        test_results = []
        
        # Check if files exist
        required_files = [
            "README.md",
            "frontend/index.html",
            "backend/main.py",
            "backend/requirements.txt",
            "tests/test_api.py",
            "Dockerfile"
        ]
        
        missing_files = []
        for file_req in required_files:
            if not (output_path / file_req).exists():
                missing_files.append(file_req)
        
        if missing_files:
            test_results.append(f"❌ Missing files: {', '.join(missing_files)}")
        else:
            test_results.append("✅ All required files present")
        
        # Check Python syntax
        try:
            result = subprocess.run([
                "python3", "-m", "py_compile", 
                str(output_path / "backend" / "main.py")
            ], capture_output=True, text=True, timeout=10)
            
            if result.returncode == 0:
                test_results.append("✅ Backend Python syntax valid")
            else:
                test_results.append(f"❌ Backend Python syntax error: {result.stderr[:100]}")
        except Exception as e:
            test_results.append(f"⚠️ Could not check Python syntax: {str(e)[:50]}")
        
        # Check if we can install dependencies (dry run)
        try:
            req_file = output_path / "backend" / "requirements.txt"
            if req_file.exists():
                test_results.append("✅ Requirements file found")
            else:
                test_results.append("❌ Requirements file missing")
        except Exception as e:
            test_results.append(f"⚠️ Requirements check failed: {str(e)[:50]}")
        
        return "; ".join(test_results)
    
