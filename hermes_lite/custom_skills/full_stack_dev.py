class FullStackDevSkill:
    """Professional full stack development workflow skill"""
    
    def __init__(self):
        self.name = "full_stack_dev"
        self.description = "Professional full stack development workflow: plan, implement, test, deploy"
        self.version = "1.0.0"
        self.author = "Hermes Lite"
    
    def execute(self, agent, project_description: str, tech_stack: str = "auto", 
                output_dir: str = "./project") -> str:
        """
        Execute full stack development workflow
        
        Args:
            agent: The Hermes Lite agent instance
            project_description: Description of what to build
            tech_stack: Preferred tech stack (auto, react-node, vue-python, etc.)
            output_dir: Directory to create the project in
            
        Returns:
            Result of the development workflow
        """
        try:
            # Phase 1: Planning
            print("🔍 Phase 1: Planning the project...")
            plan_result = self._create_project_plan(agent, project_description, tech_stack)
            
            # Phase 2: Implementation
            print("🔨 Phase 2: Implementing the project...")
            impl_result = self._implement_project(agent, plan_result, output_dir)
            
            # Phase 3: Testing
            print("🧪 Phase 3: Testing the implementation...")
            test_result = self._test_project(agent, output_dir)
            
            # Phase 4: Deployment preparation
            print("🚀 Phase 4: Preparing for deployment...")
            deploy_result = self._prepare_deployment(agent, output_dir)
            
            return f"""✅ Full stack development workflow completed!

📋 PLAN: {plan_result}
🔧 IMPLEMENTATION: {impl_result}
🧪 TESTING: {test_result}
🚀 DEPLOYMENT PREP: {deploy_result}

Project created in: {output_dir}
Next steps: Review the generated code, run tests, and deploy as needed."""
            
        except Exception as e:
            return f"❌ Error in full stack development workflow: {str(e)}"
    
    def _create_project_plan(self, agent, description: str, tech_stack: str) -> str:
        """Create a detailed project plan using the agent"""
        planning_prompt = f"""You are a senior full stack architect. Create a concise but complete plan for this project:

PROJECT: {description}
TECH STACK PREFERENCE: {tech_stack}

Provide:
1. Core features (3-5 max)
2. Tech stack recommendation (frontend/backend/database)
3. File structure outline
4. Key APIs/endpoints needed
5. Estimated complexity (simple/medium/complex)

Be concise - this is for token efficiency. Use bullet points."""
        
        try:
            plan = agent.chat(planning_prompt)
            return plan[:200] + "..." if len(plan) > 200 else plan
        except Exception as e:
            return f"Planning fallback: {description} - Basic web app with REST API"
    
